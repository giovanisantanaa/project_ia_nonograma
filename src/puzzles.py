import random

from ambiente import Nonograma


def calcular_pistas(desenho):
    linhas = len(desenho)
    colunas = len(desenho[0])

    pistas_linha = []
    for i in range(linhas):
        grupos = []
        contador = 0

        for j in range(colunas):
            if desenho[i][j] == 1:
                contador = contador + 1
            else:
                if contador > 0:
                    grupos.append(contador)
                    contador = 0

        if contador > 0:
            grupos.append(contador)
        if len(grupos) == 0:
            grupos = [0]

        pistas_linha.append(grupos)

    pistas_coluna = []

    for j in range(colunas):
        grupos = []
        contador = 0

        for i in range(linhas):
            if desenho[i][j] == 1:
                contador = contador + 1
            else:
                if contador > 0:
                    grupos.append(contador)
                    contador = 0

        if contador > 0:
            grupos.append(contador)
        if len(grupos) == 0:
            grupos = [0]

        pistas_coluna.append(grupos)

    return pistas_linha, pistas_coluna


def criar_puzzle(desenho, nome):
    pistas_linha, pistas_coluna = calcular_pistas(desenho)
    return Nonograma(pistas_linha, pistas_coluna, nome=nome)


def gerar_puzzle_aleatorio(tamanho, nome=None):
    desenho = []
    for i in range(tamanho):
        linha = []
        for j in range(tamanho):
            linha.append(random.randint(0, 1))
        desenho.append(linha)

    if nome is None:
        nome = "aleatorio " + str(tamanho) + "x" + str(tamanho)

    return criar_puzzle(desenho, nome)


