def montar_desenho_xadrez(tamanho):
    desenho = []
    for i in range(tamanho):
        linha = []
        for j in range(tamanho):
            if (i + j) % 2 == 0:
                linha.append(1)
            else:
                linha.append(0)
        desenho.append(linha)
    return desenho


